
public class Coordinates {
    private int name;
    private int x;
    private int y;

    public Coordinates(int name, int x, int y) {
        this.name = name;
        this.x = x;
        this.y = y;
    }

    public int getX() {
        return x;
    }

    public int getY() {
        return y;
    }

    public int getName() {
        return name;
    }
}
